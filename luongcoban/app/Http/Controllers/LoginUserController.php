    public function dangnhap(Request $request){
        $account = Account::where('username' ,'=',$request->get('username'))->first();
        $hashedPw = bcrypt($request->get('password'));
        $salt = $account->salt;
        if ($account != null){
            if ($account->password == $hashedPw.$salt){
                return view('dntc');
            }
            else{
                return view('dntb');
            }
        }
        else{
            return view('dntb');
        }
    }
